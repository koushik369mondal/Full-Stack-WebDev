# Variables in real life scenarios

# Example 1
book = "book on the table"
print(f"Papa ne bola ki {book} ko padho.")

# Example 2
marks = 85
print(f"Teacher ne bola ki jo {marks} diye hain unhe note kar lo.")

# Example 3
cold_drink = "Pepsi"
print(f"Bhai ne bola ki fridge me jo {cold_drink} hai wo le aao.")

# Data Types in real life

# String Example
whatsapp_message = "Hello, Kaise ho?"
print(f"WhatsApp ke messages: {whatsapp_message}")

# Integer Example
steps_count = 10000
print(f"Fitness app me steps count: {steps_count}")

# Float Example
discount_price = 399.99
print(f"Shopping app me discount prices: {discount_price}")

# Boolean Example
is_item_available = True
print(f"E-commerce app me 'is item available' status: {is_item_available}")
